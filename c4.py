"""
heapq 就是堆排序的问题 从N个元素中选择n个最大，或者最小的元素 nlargest nsmallest
heapq.nlargest(3, items, key)  key 为排序关键字, 一般情况N不会太大而n也不会接近于N的情况下
使用
"""
import heapq
nums = [1, 8, 2, 23, 7, -4, 18, 23, 42, 37, 2]
print(heapq.nlargest(3, nums)) # Prints [42, 37, 23]
print(heapq.nsmallest(3, nums)) # Prints [-4, 1, 2]

